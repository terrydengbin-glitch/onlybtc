def get_radar(module_id: str) -> RadarModule:
    for module in RADAR_MODULES:
        if module.module_id == module_id:
            return module
    raise KeyError(f"Unknown radar module: {module_id}")


def radar_metric_contract(module_id: str, rule: RadarMetricRule) -> dict[str, Any]:
    duplicate_group_id = DUPLICATE_GROUPS.get(rule.metric_id, rule.metric_id)
    return {
        "horizon_tags": list(
            METRIC_HORIZON_TAGS.get(rule.metric_id, _default_horizon_tags(module_id, rule))
        ),
        "module_weight": MODULE_WEIGHTS.get(module_id, round(1.0 / len(RADAR_MODULES), 4)),
        "duplicate_group_id": duplicate_group_id,
        "duplicate_policy": "cap_group_total_weight",
        "duplicate_group_max_weight": DUPLICATE_GROUP_MAX_WEIGHTS.get(
            duplicate_group_id,
            max(float(rule.weight or 0.0), 0.04),
        ),
        "driver_eligible": rule.driver_eligible,
    }


def _default_horizon_tags(module_id: str, rule: RadarMetricRule) -> tuple[str, ...]:
    if rule.role in {"event_context", "risk_context"}:
        return ("h24", "d3")
    if rule.role in {"cycle_context", "audit_context"}:
        return ("d7", "structural")
    if rule.role in {"price_state", "perp_state"}:
        return ("h24", "d3")
    if module_id in {"kline_orderflow", "derivatives_crowding", "trade_structure_flow"}:
        return ("h24",)
    if module_id in {
        "fund_flow",
        "macro_radar",
        "dollar_liquidity",
        "treasury_credit",
        "event_policy",
    }:
        return ("d3", "d7")
    if module_id in {"btc_adoption", "onchain_valuation"}:
        return ("d7", "structural")
    return ("d3",)
